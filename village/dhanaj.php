<h5 style="margin-left: 50px; font-family:bookman old style;">About Dhanaj</h5>

<p style="margin-left: 50px; text-align: center;">According to Census 2011 information the location code or village code of Dhanaj village is 542921. Dhanaj village is located in Umarkhed Tehsil of Yavatmal district in Maharashtra, India. It is situated 30km away from sub-district headquarter Umarkhed and 130km away from district headquarter Yavatmal. As per 2009 stats, Dhanaj village is also a gram panchayat.

The total geographical area of village is 963.33 hectares. Dhanaj has a total population of 2,594 peoples. There are about 553 houses in Dhanaj village. Umarkhed is nearest town to Dhanaj which is approximately 29km away.

